package EjemploBiFunction;

public class Animal {
    private String animal;
    private String especie;

    public Animal(String animal, String especie) {
        this.animal = animal;
        this.especie = especie;
    }

    public String getAnimal() {
        return animal;
    }

    public String getEspecie() {
        return especie;
    }
}
